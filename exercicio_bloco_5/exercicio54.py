import turtle

t = turtle.Turtle()
t.shape('turtle')
t.color('pink')
for _ in range(2):
   t.forward(100)
   t.left(45)
   t.forward(50)
   t.left(90)
   t.forward(50)
   t.left(45)

turtle.mainloop()